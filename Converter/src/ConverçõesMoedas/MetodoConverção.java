package ConverçõesMoedas;

public class MetodoConverção {

	double valor;

	double converterDolar(double quantidade) {
		return valor = quantidade / 5.165;
	}
	double dolarParaReal(double quantidade) {
		return valor = quantidade * 5.165;
	}

	double converterEuro(double quantidade) {
		return valor = quantidade / 5.541;
	}
	
	double euroPesoArgentino (double quantidade) {
		return valor = (quantidade * 5.541) / 939.441;
	}
	
	double realPesoArgentino (double quantidade) {
		return valor = quantidade * 169.492;
	}
	double pesoDolar (double quantidade) {
		return valor = (quantidade * 0.8755 ) / 0.001;
	}
	
	
	
	
	
	
}
